<img src="images/noellepicture.jpg" style="float:right; width:200px; margin: 0 0 15px 15px" /><p>Noelle Petrowski's poem "Where Snow Comes From" (Unbound, Fall 2011) was perfectly fitting for the barrage of snow we received this year. As the Dogwood trees bloomed and the birds warmed up their vocal chords, the snow created a beautiful white backdrop for flowers but stifled the birds. It is those bittersweet qualities of nature that Noelle is drawn to.</p>

<p>"I think the bittersweet is where I'm finding myself going over and over again," she says. "I'm really inspired by nature, but I also think that humans screw it up a lot. I think that's often something I play with [when writing]."</p>
 
<p>A native of Beaverton, Oregon, Noelle admires the nature and scenery of the Pacific Northwest and can't help but include it in her poetry. "You know the Portlandia skit 'Put a Bird on It'? That's me!" she says.</p>
 
<p>Other students in the Kidd Tutorial, UO's yearlong creative writing intensive, playfully tease her about her obsession with nature, but she can't help but write about our connection to it. "I like the duality of nature," she says. "I think humans are a part of it even though a lot of humans don't think so."</p>
 
<p>Poetry first captivated Noelle in high school, but she didn't know where it would lead. She planned to study Architecture but decided it wasn't for her. Then she took her first creative writing class in poetry and thought "it was so fun that I kept taking classes and kept hearing things about submitting to Unbound or joining the Kidd Tutorial, and I looked into it and signed up." Noelle says joining the Kidd Tutorial is "the best choice I've made in college so far" and when she received the e-mail from Unbound that her poem "The Nest" would be published in the Winter 2011 issue, she "freaked out and was so excited."</p>
 
<p>Although Noelle isn't sure about what she wants to do after college, she has hope that "if I really like what I'm doing I'll find something that will fit in"&mdash;but Noelle has a dream.</p>
 
<p>"I really want to be a farmer&hellip;like really, really bad."</p>
 
<p>Noelle's dream is to "have a plot of land, pigs and goats," where she could "grow things and eat things and write poetry about those things." She realizes this may be a farfetched goal and says, "People have definitely told me this is weird thing." But Noelle and her boyfriend Paul are doing what they can to keep their dream alive and prepare themselves should they ever have the opportunity. They have a shelf of books with titles such as <em>The Urban Homestead</em>, <em>How to Farm at Home</em>, and <em>How to Take Care of Pigs</em>. "That's all we want to do," she says. "Our parents have slowly gotten used to it." Noelle has found that life is not about riches and wealth, adding that she would be entirely fulfilled "growing my own beets, having a pig, and writing poetry."</p>
 
<p>You can find Noelle online at her blog, <a class="header_color" href="http://noellephant.blogspot.com">Noellephant</a>, and read samples of her poetry below. </p>
