<h3>THE NEST</h3>
<p>Unbound, Winter 2011</p>

<p>The plum orchards on Orcas Island<br/>
bore fruit, soft like velvet.<br/>
I walked where the trees grew thicker<br/>
and the sun, through the branches, flickered.<br/>
Behind a bush where the green had faded away</p>

<p>I found two baby fawns. Lifeless;<br/>
entangled with each other like a nest.<br/>
Both with delicate, knobby knees,<br/>
a sister and a brother, with eyes<br/>
that will never open, to see the other.</p>
 
<h3>CHRYSANTHEMUM SUMMER</h3>
<p>Unbound, Spring 2011</p>

<p>You stained me, but I forgive you<br/>
for the blackberry juice that dripped down my legs,<br/>
tinting my new white shorts, unseen,<br/>
sweating behind a pale birch tree<br/>
while we played hide and seek in Cook Park.<br/>
I saw dust rising from the weight of my steps<br/>
on the trail, that morning I grew up.<br/>
The chrysanthemums tickled my nose<br/>
and the sweltering heat on my back<br/>
cooled by the time our sunset wade<br/>
into the lagoon in front of Apple Court<br/>
bathed us in pink light. My shorts burned, tainted,<br/>
but I did not look you in the eye.<br/>
And I did not want to tell the cascade of frogs<br/>
we caught that they live in a lie.<br/>
I now see the delicate chrysanthemums<br/>
&mdash;the way they fill the space of my palm&mdash;<br/>
that only haunt me now that you have gone.</p>


<h3>WHERE SNOW COMES FROM</h3>
<p>Unbound, Fall 2011</p>

<p>The cold sank in before I could feel it,<br/>
before my fingers stopped bending<br/>
as if they had been filled with water<br/>
and, in this storm, had frozen.<br/>
I think the birds went next<br/>
although they never did wrong to anyone<br/>
murmuring on wires over miles<br/>
of mindless river. They were too light,<br/>
their twig-like bones too hollow,<br/>
ready to snap, and the great frost<br/>
swallowed them up. As I walked<br/>
I could barely hear them<br/>
as they shattered behind me.</p>

